    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) Table.getModel();
        modelo.setNumRows(0);
        pessoaDao pdao = new pessoaDao();
        for (pessoaModel p : pdao.read()) {
            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome(),
                p.getEmail()
            });
        }
    }
    
