    private void AddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddActionPerformed
        
        pessoaModel p = new pessoaModel();
        pessoaDao dao = new pessoaDao();
        p.setNome(txtNome.getText());
        p.setEmail(txtEmail.getText());
        dao.create(p);
        
        txtNome.setText("");
        txtEmail.setText("");

        txtNome.requestFocus();

        readJTable();
    }//GEN-LAST:event_AddActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        
        if (Table.getSelectedRow() != -1) {
            
            pessoaModel p = new pessoaModel();
            pessoaDao dao = new pessoaDao();

            p.setId((int)Table.getValueAt(Table.getSelectedRow(), 0));
            dao.delete(p);

            txtNome.setText("");
            txtEmail.setText("");
            
            txtNome.requestFocus();

            readJTable();
            
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma Pessoa para excluir.");
        }
    }//GEN-LAST:event_ExcluirActionPerformed

    private void AlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AlterarActionPerformed
        
        if (Table.getSelectedRow() != -1) {

            pessoaModel p = new pessoaModel();
            pessoaDao dao = new pessoaDao();
            p.setNome(txtNome.getText());
            p.setEmail(txtEmail.getText());
            p.setId((int)Table.getValueAt(Table.getSelectedRow(), 0));
            dao.update(p);

            txtNome.setText("");
            txtEmail.setText("");

            txtNome.requestFocus();

            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Selecione uma Pessoa para Alterar.");
        }

    }//GEN-LAST:event_AlterarActionPerformed

    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        if (Table.getSelectedRow() != -1) {
            txtNome.setText(Table.getValueAt(Table.getSelectedRow(), 1).toString());
            txtEmail.setText(Table.getValueAt(Table.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_TableMouseClicked

    private void TableKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TableKeyReleased
        if (Table.getSelectedRow() != -1) {
            txtNome.setText(Table.getValueAt(Table.getSelectedRow(), 1).toString());
            txtEmail.setText(Table.getValueAt(Table.getSelectedRow(), 2).toString());
        }
    }//GEN-LAST:event_TableKeyReleased


